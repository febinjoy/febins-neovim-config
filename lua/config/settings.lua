vim.cmd("set softtabstop=2")
vim.cmd("set shiftwidth=2")
vim.cmd("set encoding=utf-8") -- set encoding
vim.cmd("set nu") -- enable line numbers
vim.cmd("set relativenumber") -- enable relative line numbers
vim.cmd("set autoindent") -- enable auto indentation
vim.cmd("set list") -- show tab characters and trailing whitespace
vim.cmd("set ignorecase") -- ignore case when searching
vim.cmd("set smartcase") -- unless capital letter in search
vim.cmd("set nohlsearch") -- do not highlight all matches on previous search pattern
vim.cmd("set incsearch") -- incrementally highlight searches as you type
vim.cmd("set termguicolors") -- minimum number of lines to keep above and below the cursor
vim.cmd("set scrolloff=8") -- minimum number of lines to keep above and below the cursor
vim.cmd("set sidescrolloff=8") -- minimum number of columns to keep above and below the cursor
